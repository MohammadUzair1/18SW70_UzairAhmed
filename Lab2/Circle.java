class Circle
{
	 public static void main(String[] args) {
	 	  double PI=3.14;
	 	  double radius=5;
	 	  double circumference=2*PI*radius;
	 	  double diameter=2*radius;
	 	  double area=PI*radius*radius;
	 	  System.out.println("Circumference of a circle: "+circumference+" units");
	 	  System.out.println("Diameter of a circle: "+diameter+" units");
	 	  System.out.println("Area of the circle: "+area+" square unit");
	 }
}